# UNION


x = {"Rolls", "Bmw", "Mersedez"}
y = {"Nokia", "Redmi", "Iphone"}

z = x.union(y) 

print(z)


# UPDATE


x = {"apple", "banana", "cherry"}
y = {"google", "microsoft", "apple"}

x.update(y) 

print(x)


# ADD
thisset = {"apple", "banana", "cherry"}

thisset.add("orange")

print(thisset)


# POP
fruits = {"apple", "banana", "cherry"}

fruits.pop() 

print(fruits)


# REMOVE
fruits = {"apple", "banana", "cherry"}

fruits.remove("banana") 

print(fruits)

# COPY
fruits = {"apple", "banana", "cherry"}

x = fruits.copy()

print(x)

# CLEAR

thisset = {"apple", "banana", "cherry"}

thisset.clear()

print(thisset)

# DIFFERENCE

x = {"apple", "banana", "cherry"}
y = {"google", "microsoft", "apple"}

z = x.difference(y) 

print(z)












