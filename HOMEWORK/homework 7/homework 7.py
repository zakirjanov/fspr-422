# INDEX

# Метод возвращает позицию при первом вхождении указанного значения.index()
fruits = [33, 45, 640, 907, 900, 500]

x = fruits.index(500)

print(x)


# POP

# Метод удаляет элемент в указанной позиции.pop()

car = ['kia', 'tesla', 'cherry']

x = car.pop(1)

print(x)

# EXTEND

# Метод добавляет указанные элементы списка (или любые итерации) в конец текущего списка.extend()

car = ['kia', 'tesla', 'cherry']

points = (6, 7, 8, 3)

car.extend(points)

print(car)


# INSERT

# Метод вставляет указанное значение в указанную позицию.insert()

fruits = ['apple', 'banana', 'cherry']

fruits.insert(1, "orange")

print(fruits)


# COPY

# Метод возвращает копию указанного списка.copy()

programs = ["HTML", "CSS", "PYTON"]

x = programs.copy()

print(x)


# COUNT

# Метод возвращает количество элементов с заданным значением.count()

fruits = [1, 4, 2, 9, 7, 8, 9, 3, 1]

x = fruits.count(9)

print(x)


# APPEND

# Метод добавляет элемент в конец списка.append()

a = ["apple", "banana", "cherry"]

b = ["Ford", "BMW", "Volvo"]

a.append(b)

print(a)


