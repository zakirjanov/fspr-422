# INDEX

# Метод возвращает позицию при первом вхождении указанного значения.index()
fruits = [33, 45, 640, 907, 900, 500]

x = fruits.index(500)

print(x)







# COUNT

# Метод возвращает количество элементов с заданным значением.count()

fruits = [1, 4, 2, 9, 7, 8, 9, 3, 1]

x = fruits.count(9)

print(x)